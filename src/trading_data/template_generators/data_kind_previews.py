JSON_TEMPLATES: tuple[JsonTemplate, ...] = ()


def all_template_paths() -> list[Path]:
    """Return all generated preview template paths relative to the template root."""
    return [template.relative_path for template in CSV_TEMPLATES] + [
        template.relative_path for template in JSON_TEMPLATES
    ]


def write_csv_template(template: CsvTemplate, registry: Registry, template_root: Path) -> None:
    path = template_root / template.relative_path
    path.parent.mkdir(parents=True, exist_ok=True)
    headers = [registry.payload(column) for column in template.columns]
    values = [resolve(template.row.get(column, ""), registry) for column in template.columns]
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.writer(handle, lineterminator="\n")
        writer.writerow(headers)
        writer.writerow(values)


def write_json_template(template: JsonTemplate, registry: Registry, template_root: Path) -> None:
    path = template_root / template.relative_path
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(resolve(template.payload, registry), indent=2) + "\n", encoding="utf-8")


def generate_templates(
    *, registry_csv: Path = DEFAULT_REGISTRY_CSV, template_root: Path = DEFAULT_TEMPLATE_ROOT
) -> list[Path]:
    """Generate all final data-kind preview templates and return written paths."""
    registry = Registry(registry_csv)
    written: list[Path] = []
    for template in CSV_TEMPLATES:
        write_csv_template(template, registry, template_root)
        written.append(template_root / template.relative_path)
    for template in JSON_TEMPLATES:
        write_json_template(template, registry, template_root)
        written.append(template_root / template.relative_path)
    return written


def check_templates(*, registry_csv: Path, template_root: Path) -> list[Path]:
    """Return committed template paths that differ from generated output."""
    with tempfile.TemporaryDirectory() as temp_dir:
        generated_root = Path(temp_dir)
        generate_templates(registry_csv=registry_csv, template_root=generated_root)
        changed: list[Path] = []
        for relative_path in all_template_paths():
            current = template_root / relative_path
            generated = generated_root / relative_path
            if not current.exists() or not filecmp.cmp(current, generated, shallow=False):
                changed.append(current)
        return changed


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--registry-csv", type=Path, default=DEFAULT_REGISTRY_CSV)
    parser.add_argument("--template-root", type=Path, default=DEFAULT_TEMPLATE_ROOT)
    parser.add_argument(
        "--check",
        action="store_true",
        help="fail if committed templates differ from generated output",
    )
    args = parser.parse_args(argv)

    if args.check:
        changed = check_templates(registry_csv=args.registry_csv, template_root=args.template_root)
        if changed:
            print("data-kind preview templates are stale:")
            for path in changed:
                print(path)
            return 1
        print("data-kind preview templates are current")
        return 0

    written = generate_templates(registry_csv=args.registry_csv, template_root=args.template_root)
    for path in written:
        print(path.relative_to(REPO_ROOT) if path.is_relative_to(REPO_ROOT) else path)
    return 0


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main())
