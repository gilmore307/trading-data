class Registry:
    """Read current registry payloads by stable id."""

    def __init__(self, registry_csv: Path):
        with registry_csv.open(newline="", encoding="utf-8") as handle:
            rows = list(csv.DictReader(handle))
        self._by_id = {row["id"]: row for row in rows}

    def payload(self, ref: RegistryRef) -> str:
        row = self._by_id.get(ref.id)
        if row is None:
            raise KeyError(f"Registry id not found: {ref.id}")
        if row["kind"] != ref.expected_kind:
            raise ValueError(
                f"Registry id {ref.id} expected kind={ref.expected_kind}, got kind={row['kind']}"
            )
        return row["payload"]


def resolve(value: Any, registry: Registry) -> Any:
    """Resolve registry refs recursively in generated template specs."""
    if isinstance(value, RegistryRef):
        return registry.payload(value)
    if isinstance(value, Mapping):
        return {resolve(key, registry): resolve(child, registry) for key, child in value.items()}
    if isinstance(value, list):
        return [resolve(child, registry) for child in value]
    if isinstance(value, tuple):
        return [resolve(child, registry) for child in value]
    return value


def csv_template(relative_path: str, columns: Sequence[RegistryRef], values: Sequence[Any]) -> CsvTemplate:
    if len(columns) != len(values):
        raise ValueError(f"CSV template {relative_path} has mismatched columns and values")
    return CsvTemplate(Path(relative_path), columns, dict(zip(columns, values)))


